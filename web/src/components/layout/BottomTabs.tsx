const TABS = [
  { href: "/", label: "Games", Icon: GamesIcon, adminOnly: false },
  { href: "/golf", label: "Golf", Icon: GolfIcon, adminOnly: false },
  { href: "/fairbet", label: "FairBet", Icon: FairBetIcon, adminOnly: false },
  { href: "/analytics", label: "Analytics", Icon: AnalyticsIcon, adminOnly: false },
  { href: "/history", label: "History", Icon: HistoryIcon, adminOnly: true },
];

export function BottomTabs() {
  const pathname = usePathname();
  const openSettings = useUI((s) => s.openSettings);
  const role = useAuth((s) => s.role);
  const visibleTabs = TABS.filter((t) => !t.adminOnly || role === "admin");
  return (
    <nav data-testid="bottom-tabs" className="fixed bottom-0 left-0 right-0 z-50 border-t border-neutral-800 bg-neutral-950/95 backdrop-blur pb-[env(safe-area-inset-bottom)] md:hidden">
      <div className="flex h-16 items-center justify-around">
        {visibleTabs.map((tab) => {
          const isActive =
            tab.href === "/"
              ? pathname === "/"
              : pathname.startsWith(tab.href);
          return (
            <Link
              key={tab.href}
              href={tab.href}
              className={cn(
                "flex flex-col items-center gap-1 text-xs transition",
                isActive ? "text-neutral-50" : "text-neutral-500",
              )}
            >
              <tab.Icon />
              {tab.label}
            </Link>
          );
        })}
        <button
          onClick={openSettings}
          className="flex flex-col items-center gap-1 text-xs text-neutral-500 transition"
        >
          <SettingsIcon />
          Settings
        </button>
      </div>
    </nav>
  );
}
